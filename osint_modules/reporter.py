        overview = []
        
        # Data types analyzed
        data_types = set()
        for dataset in datasets:
            if 'ip' in dataset:
                data_types.add('IP Address')
            if 'domain' in dataset:
                data_types.add('Domain')
            if 'username' in dataset:
                data_types.add('Social Media')
        
        overview.append(f"â€¢ Data Types Analyzed: {', '.join(data_types)}")
        overview.append(f"â€¢ Total Records: {len(datasets)}")
        
        # Risk distribution
        risk_counts = {'high': 0, 'medium': 0, 'low': 0}
        for dataset in datasets:
            risk_level = dataset.get('risk_level', 'low')
            risk_counts[risk_level] += 1
        
        overview.append(f"â€¢ Risk Distribution: High ({risk_counts['high']}), Medium ({risk_counts['medium']}), Low ({risk_counts['low']})")
        
        return '\n'.join(overview)
    
